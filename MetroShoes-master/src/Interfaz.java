    private void ToggleEnsamblajeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ToggleEnsamblajeMouseClicked
        // TODO add your handling code here:

        if(ToggleEnsamblaje.getValue() == 1)
        {   
            if(listaEnsam.size() == 1){
                listaEnsam.add(1, new Ensamblar(BufTrenza, BufSuela, BufPlantilla, ProducTrenza, ConsumTrenza, ProducSuela, ConsumSuela, ProducPlantilla, ConsumPlantilla, ProgressBarT, ProgressBarS, ProgressBarP, "Ensamblador 2",AreaEnsamblaje1, AreaProduccion, Ptipo1, Ptipo2 ));
                listaEnsam.get(1).start();
                AreaEnsamblaje1.append("****** Se Agrego un Ensamblador ******\n");
            }  
        }
         if(ToggleEnsamblaje.getValue() == 0)
        {
            listaEnsam.get(1).stop();
            listaEnsam.remove(1);
            AreaEnsamblaje1.append("****** Se Quito un Ensamblador ******\n");
        }
    }//GEN-LAST:event_ToggleEnsamblajeMouseClicked

