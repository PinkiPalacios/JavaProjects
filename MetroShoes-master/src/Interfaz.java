    public void pararProd (){
        int i = listaProdT.size()-1; 
        while(i >= 0){
            listaProdT.get(i).stop();
            AreaProduccion.append("-Productor de Trenzas "+i+" parado-\n");
            listaProdT.remove(i);
            i--;
        }
        i = listaProdS.size()-1; 
        while(i >= 0){
            listaProdS.get(i).stop();
            AreaProduccion.append("-Productor de Suelas "+i+" parado-\n");
            listaProdS.remove(i);
            i--;
        }
        i = listaProdP.size()-1; 
        while(i >= 0){
            listaProdP.get(i).stop();
            AreaProduccion.append("-Productor de Plantillas "+i+" parado-\n");
            listaProdP.remove(i);
            i--;
        }
        
        
    }  

