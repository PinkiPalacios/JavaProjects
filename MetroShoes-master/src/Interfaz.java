    public void agregarProdT(int a){
        while(a > 0){
        listaProdT.add(new ProdTrenza(BufTrenza, Integer.toString(listaProdT.size())  ,ProducTrenza, ConsumTrenza, ProgressBarT,AreaProduccion));
        listaProdT.get(listaProdT.size()-1).start();
        
        AreaProduccion.append("+Nuevo productor de Trenzas+ "+(listaProdT.size()-1)+"\n");
        a--;
        try {
            Thread.sleep(200);                
        } catch(InterruptedException ex) {
            Thread.currentThread().interrupt();
        }
        }
    }
    
