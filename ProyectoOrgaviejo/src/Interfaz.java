     public void agregarPers (Persona pers){
         
          if (vecP[0] == null){
            
                vecP[0] = pers;
                
                
            }
          else if(vecP[0].getCI()>pers.getCI() && vecP[1]==null){
          vecP[1]=vecP[0];
          vecP[0]=pers;
              
          } else if(pers.getCI()<vecP[0].getCI()){
              
              CorrerVec(0);
          vecP[0]=pers;
          
          }
          else{
        for (int i = 0; i < 30; i++) {
            int a=i+1;
           
             if (vecP[i].getCI() < pers.getCI() && vecP[a]==null){
        
                vecP[a]=pers;
               return;
        }
                
            else if (vecP[i].getCI() < pers.getCI() && vecP[a].getCI() > pers.getCI()){
            
                CorrerVec(a);
                
                vecP[a] = pers;
            return;
            }
            

        }
    
    }}
     
     
     public void CorrerVec (int a){
     
     int x = 28;
     
     while (x >= a){
     
         int y = x+1;
         
         vecP[y] = vecP[x];
         x--;
     
     }
     
     }
    
